export class Point{
    x: number;
    y: number;
    constructor(x: number = 0, y: number = 0){
        this.x = x;
        this.y = y;
    }
}

export class Size{
    width: number;
    height: number;
    constructor(w: number = 0, h: number = 0){
        this.width = w;
        this.height = h;
    }
}